// Vollständiges Tarot-Deck: 56 Karten der Kleinen Arkana in 4 Suiten.
// Wird zusammen mit der Großen Arkana (im tarot_oracle_screen) zum
// 78-Karten-Deck kombiniert.

library;

enum TarotSuit { wands, cups, swords, pentacles, major }

class TarotCard {
  final int index; // 0-21 fuer Major, 1-14 innerhalb der Suite fuer Minor
  final String name; // Deutsche Bezeichnung
  final String nameEn; // English name (fuer AI-Lesungs-Prompt)
  final TarotSuit suit;
  final String emoji;
  final String element; // Feuer / Wasser / Luft / Erde / Aether
  final String meaning; // Aufrechte Bedeutung
  final String reversedMeaning;
  final List<String> keywords;

  const TarotCard({
    required this.index,
    required this.name,
    required this.nameEn,
    required this.suit,
    required this.emoji,
    required this.element,
    required this.meaning,
    required this.reversedMeaning,
    this.keywords = const [],
  });

  String get suitDE => switch (suit) {
        TarotSuit.wands => 'Stäbe',
        TarotSuit.cups => 'Kelche',
        TarotSuit.swords => 'Schwerter',
        TarotSuit.pentacles => 'Münzen',
        TarotSuit.major => 'Große Arkana',
      };

  String get fullId =>
      suit == TarotSuit.major ? 'major_$index' : '${suit.name}_$index';
}

