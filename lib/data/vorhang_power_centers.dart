import 'package:flutter/material.dart';

class PowerCenter {
  final String name;
  final String description;
  final String badge;
  final String founded;
  final String influence;
  final List<String> members;
  final List<String> connections;
  final double lat;
  final double lng;
  final String? imageUrl;
  final String category; // 'thinktank' | 'finance' | 'religion' | 'intel' | 'secret' | 'media' | 'tech' | 'edu'

  const PowerCenter({
    required this.name,
    required this.description,
    required this.badge,
    required this.founded,
    required this.influence,
    required this.lat,
    required this.lng,
    this.members = const [],
    this.connections = const [],
    this.imageUrl,
    this.category = 'thinktank',
  });

  IconData get icon {
    switch (category) {
      case 'finance':
        return Icons.account_balance;
      case 'religion':
        return Icons.church;
      case 'intel':
        return Icons.shield;
      case 'secret':
        return Icons.lock;
      case 'media':
        return Icons.broadcast_on_personal;
      case 'tech':
        return Icons.precision_manufacturing;
      case 'edu':
        return Icons.school;
      default:
        return Icons.psychology;
    }
  }
}

