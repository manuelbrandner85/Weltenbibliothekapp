# Reiki - Die Kraft der Hände

## Was ist Reiki?
**Reiki** (霊気, "universelle Lebensenergie") ist eine japanische Heilmethode, entwickelt von **Mikao Usui** (1865-1926), bei der durch Handauflegen Lebensenergie übertragen wird.

## Die Geschichte

### Mikao Usui
- **1922:** Erleuchtung auf Berg Kurama (Kyoto)
- 21 Tage Fasten & Meditation
- Empfing Reiki-Symbole und Heilfähigkeit
- Gründete **Usui Reiki Ryoho Gakkai**

### Verbreitung im Westen
- **Hawayo Takata** brachte Reiki in die USA (1938)
- **Heute:** Über 1 Million Reiki-Praktizierender weltweit

## Die 5 Reiki-Prinzipien

Usui's **Lebensregeln (Gokai):**

> Gerade heute:
> 1. Ich werde nicht zornig sein
> 2. Ich werde mir keine Sorgen machen
> 3. Ich werde dankbar sein
> 4. Ich werde meine Arbeit ehrlich verrichten
> 5. Ich werde freundlich zu allen Lebewesen sein

